#include "libft.h"

void	ft_deltab_int(int **t)
{
	int	i;

	i = 0;
	if (t != NULL)
	{
		while (t != NULL)
		{
			free(t[i]);
			i++;
		}
		free(t[i]);
		free(t);
	}
}
